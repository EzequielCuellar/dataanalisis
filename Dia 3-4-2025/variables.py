# ¿Qué es una Variable?
#Contenedor de Datos=Una variable es como una "caja" donde almacenamos datos temporales.
#Etiqueta Única=Cada variable tiene un nombre único que permite acceder a su contenido.
#Almacenamiento Dinámico=Las variables permiten que los programas sean flexibles y dinámicos.
#Funcionamiento de Variables en Python
#Tipado Dinámico=Python no requiere declarar explícitamente el tipo de dato de una variable.
#Asignación Simple=Se utiliza el operador '=' para asignar un valor a una variable.
# Formas de Asignación
#Asignación Única=Se asigna un solo valor a una variable. Ejemplo: edad = 30
#Asignaciones Múltiples=Se asignan valores a varias variables en una línea. Ejemplo: x, y, z = 1, 2, 3
#Cambio de Valor=Se puede modificar el valor de una variable existente. Ejemplo: contador = contador + 1

